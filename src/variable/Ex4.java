package variable;

public class Ex4 {
    public static void main(String[] args) {
        int number = 10;
        double decimal = 3.14;
        char letter ='A';
        boolean isTrue = true;

        number = 5;
        System.out.println(number);
        decimal = 2.71828;
        System.out.println(decimal);
        letter ='B';
        System.out.println(letter);
        isTrue = false;
        System.out.println(isTrue);
    }
}
