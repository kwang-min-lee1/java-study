package operator;

public class Operator2 {
    public static void main(String[] args) {
        // 문자열 더하기
        // 문자열에도 + 연산자를 사용하여 연결할 수 있다.

        String result1 = "hello" + "java";
        System.out.println("result1=" + result1 );

        //믄자열과 숫자 더하기 => 문자열
        String result2 = "hello " + 20;
        System.out.println("result2=" + result2);

    }
}
